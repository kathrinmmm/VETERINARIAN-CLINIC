package Utils;
/**
 * Enum representing different types of animals.
 * <p>
 * This enum is used to categorize animals into various types such as
 * DOG, CAT, BIRD, and REPTILE. It can be used in various places such as
 * in animal management systems, veterinary records, or classification systems.
 * </p>
 */
public enum AnimalType {
    /**
     * Represents a dog animal type.
     */
    DOG,
    /**
     * Represents a cat animal type.
     */
    CAT,
    /**
     * Represents a bird animal type.
     */
    BIRD,
    /**
     * Represents a reptile animal type.
     */
    REPTILE
}