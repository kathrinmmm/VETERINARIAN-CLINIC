package Utils;

/**
 * Enum representing the different types of vaccines available for animals.
 * <p>
 * Each vaccine type corresponds to a specific disease or infection that animals may
 * be vaccinated against, to prevent serious health issues and improve overall well-being.
 * </p>
 */
public enum VaccineType {

    /**
     * Rabies vaccine, used to protect animals against the rabies virus, a deadly
     * disease that affects the nervous system and can be transmitted to humans.
     */
    RABIES,

    /**
     * Distemper vaccine, used to protect animals, particularly dogs, from distemper,
     * a viral disease that affects the respiratory, gastrointestinal, and nervous systems.
     */
    DISTEMPER,

    /**
     * Feline Leukemia vaccine, used to protect cats from feline leukemia virus (FeLV),
     * a disease that affects cats' immune system and can lead to various serious health issues.
     */
    FELINE_LEUKEMIA,

    /**
     * Parrot Fever vaccine, used to protect birds from psittacosis (also known as parrot fever),
     * an infectious disease that can affect both birds and humans, causing respiratory issues.
     */
    PARROT_FEVER;
}
